print("Введите значения (используйте стандартные единицы СИ):")
print("Давление (P) - в паскалях (Па)")
print("Объем (V) - в кубических метрах (м³)")
print("Температура (T) - в кельвинах (К)")

P = float(input("Давление (Па): "))
V = float(input("Объем (м³): "))
T = float(input("Температура (К): "))

R = 8.314  # Дж/(моль·К)
n = (P * V) / (R * T)

print("Количество газа:", n, "моль")